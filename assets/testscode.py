def convert_to_base(n, b):  # Объявляем функцию для перевода числа в другую систему счисления
  digits = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ"  # Строка со всеми возможными цифрами (до 36-ричной системы)
  result = ""  # Создаем пустую строку для результата

  while n > 0:  # Пока наше число больше нуля, выполняем цикл
    remainder = n % b  # Находим остаток от деления числа на основание системы
    result = digits[remainder] + result  # Берем символ из строки digits под номером остатка и добавляем его В НАЧАЛО результата
    n = n // b  # Делим число нацело на основание системы, уменьшая его
  print(result)  # Выводим результат на экран
  return result  # Возвращаем результат из функции (чтобы можно было использовать дальше в программе)

convert_to_base(112, 4)  # Вызываем функцию: переводим число 112 в четверичную систему (основание 4)
convert_to_base(2315, 9)  # Вызываем функцию: переводим число 2315 в девятиричную систему (основание 9)